// Identifier Type: An identifier - identifies some entity uniquely and unambiguously. Typically this is used for business identifiers.
//
// Need to be able to identify things with confidence and be sure that the identification is not subject to misinterpretation.
type Identifier struct {
	// Unique id for the element within a resource (for internal references). This may be any string value that does not contain spaces.
	Id *string
	// May be used to represent additional information that is not part of the basic definition of the element. To make the use of extensions safe and managable, there is a strict set of governance applied to the definition and use of extensions. Though any implementer can define an extension, there is a set of requirements that SHALL be met as part of the definition of the extension.
	Extension []Extension
	// The purpose of this identifier.
	Use *Code
	// A coded type for the identifier that can be used to determine which identifier to use for a specific purpose.
	Type *CodeableConcept
	// Establishes the namespace for the value - that is, an absolute URL that describes a set values that are unique.
	System *Uri
	// The portion of the identifier typically relevant to the user and which is unique within the context of the system.
	Value *String
	// Time period during which identifier is/was valid for use.
	Period *Period
	// Organization that issued/manages the identifier.
	Assigner *Reference
}

func (r Identifier) MemSize() int {
	s := int(reflect.TypeOf(r).Size())
	if r.Id != nil {
		s += len(*r.Id) + int(reflect.TypeOf(*r.Id).Size())
	}
	for _, i := range r.Extension {
		s += i.MemSize()
	}
	s += (cap(r.Extension) - len(r.Extension)) * int(reflect.TypeOf(Extension{}).Size())
	if r.Use != nil {
		s += r.Use.MemSize()
	}
	if r.Type != nil {
		s += r.Type.MemSize()
	}
	if r.System != nil {
		s += r.System.MemSize()
	}
	if r.Value != nil {
		s += r.Value.MemSize()
	}
	if r.Period != nil {
		s += r.Period.MemSize()
	}
	if r.Assigner != nil {
		s += r.Assigner.MemSize()
	}
	return s
}
func (r Identifier) String() string {
	buf, err := json.MarshalIndent(r, "", "  ")
	if err != nil {
		return "null"
	}
	return string(buf)
}
func (r Identifier) MarshalJSON() ([]byte, error) {
	var b bytes.Buffer
	err := r.marshalJSON(&b)
	if err != nil {
		return nil, err
	}
	return b.Bytes(), nil
}
