func (r Expression) Equal(other fhirpath.Element, _noReverseTypeConversion ...bool) bool {
	var o Expression
	switch other := other.(type) {
	case Expression:
		o = other
	case *Expression:
		o = *other
	default:
		return false
	}
	eq := r.Children().Equal(o.Children())
	if eq == nil {
		return true
	}
	return *eq
}
